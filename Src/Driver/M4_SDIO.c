/*
	获取卡片信息
*/
HAL_StatusTypeDef SD_GetCardInfo(HAL_SD_CardInfoTypeDef *pCardInfo)
{
	HAL_StatusTypeDef BackVal =HAL_ERROR;
	if(HAL_SD_GetCardInfo(&hsd, pCardInfo) ==HAL_OK)
	{
		BackVal = HAL_OK;
	}
	else
	{
		BackVal = HAL_ERROR;
		return BackVal;
	}
	if(BackVal ==HAL_OK)
	{
		if( pCardInfo->BlockNbr ==0)	//查询数据块数量
		{
			BackVal =HAL_ERROR;
		}
		else
		{
			BackVal =HAL_OK;
		}
	}
	return BackVal;


	

}








